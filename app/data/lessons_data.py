def _build_content(ld: dict) -> str:
    """Build lesson content text from structured sections."""
    parts = []
    if ld.get("learning_objective"):
        parts.append("## Оқу мақсаты (Цель урока)\n" + ld["learning_objective"])
    if ld.get("section1_kz"):
        parts.append("## 1. Түсініктеме (Объяснение на казахском)\n" + ld["section1_kz"])
    if ld.get("section2_ru"):
        parts.append("## 2. Объяснение (по-русски)\n" + ld["section2_ru"])
    if ld.get("section3_examples"):
        parts.append("## 3. Мысалдар (Примеры)\n" + ld["section3_examples"])
    if ld.get("section4_mistakes"):
        parts.append("## 4. Жиі қателер (Частые ошибки)\n" + ld["section4_mistakes"])
    if ld.get("grammar_focus"):
        parts.append("## Грамматикалық нүкте\n" + ld["grammar_focus"])
    if ld.get("practical_relevance"):
        parts.append("## Тәжірибеде қолдану (Практическое применение)\n" + ld["practical_relevance"])
    return "\n\n".join(parts) if parts else ld.get("content", "")


def get_lessons() -> list[dict]:
    """Return all lesson definitions with content and vocab."""
    raw = _get_lessons_raw()
    out = []
    for ld in raw:
        content = ld.get("content")
        if not content and (
            ld.get("section1_kz") or ld.get("section2_ru") or ld.get("learning_objective")
        ):
            content = _build_content(ld)
        out.append({
            "title": ld["title"],
            "level": ld["level"],
            "topic": ld["topic"],
            "content": content or "",
            "vocab": ld.get("vocab", []),
            "order_index": ld["order_index"],
        })
    return out


