from typing import Iterator

def _w(kz: str, ru: str, tr: str | None = None, ex: str | None = None) -> dict:
    return {"word_kz": kz, "translation_ru": ru, "transcription": tr, "example_sentence": ex}


def get_vocabulary() -> list[dict]:
    """Return all vocabulary items. Deduplicated by word_kz."""
    seen: set[str] = set()
    out: list[dict] = []
    for item in _iter_vocabulary():
        kz = item["word_kz"].strip().lower()
        if kz and kz not in seen:
            seen.add(kz)
            out.append(item)
    return out


